    public FrBuscarDietasActivas() {
        initComponents();
    
            dietaData dd = new dietaData();
        ArrayList<Dieta> templm = new ArrayList();
        templm = dd.buscarDietasActivas();
        String datos[] = new String[9];
        DefaultTableModel modelo = (DefaultTableModel) this.gridDietasAct.getModel();
        modelo.setNumRows(0);
        for (Dieta aux : templm) {
            datos[0] = aux.getId_Dieta() + "";
            datos[1] = aux.getPaciente().getApellido().toString();
            datos[2] = aux.getPaciente().getNombre().toString();
            datos[3] = aux.getInicioDieta().toString();
            datos[4] = aux.getFinDieta().toString();
            datos[5] = aux.getPesoInicial() + "";
            datos[6] = aux.getPesoBuscado() + "";
            datos[7] = aux.getLimiteCalorico() + "";
            datos[8] = aux.getPaciente().getTelefono()+"";
            modelo.addRow(datos);

        }

    
    
    }

